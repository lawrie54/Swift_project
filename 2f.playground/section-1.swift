// lesson2f The if Conditional Statement and the Comparative Operators // 

let number1 = 1000
let number2 = 2000

if number1 == number2 {
    println("\(number1) == \(number2)")
}

if number1 != number2 {
    println("\(number1) != \(number2)")
}

if number1 < number2 {
    println("\(number1)< \(number2)")
}

if number1 > number2 {
    println("\(number1) > \(number2)")
}

if number1 <= number2 {
    println("\(number1) <= \(number2)")
}

if number1 >= number2 {
    println("\(number1) >= \(number2)")
}

// control statements require {}. we also use string literals for number1 and number2